class Q1  
{  
public static void main(String[] args)   
{  
int i, num = 50, sum = 0;    
for(i = 1; i <= num; ++i)  
{   
sum = sum + i;  
}   
System.out.println("Sum of First 50 Natural Numbers is = " + sum);  
}  
}  



